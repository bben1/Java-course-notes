package tutorial4_9_ControlFlow;

public class Tutorial_5_ForLoops {

	public static void main(String[] args) {
		
		for (int i=0; i<10; i++ ) {//i++ means i = i + 1
			//(executed before loop;condition for loop to execute;executed after the loop)
			System.out.printf("The value of i is: %d \n",  i); //print formatting in java
		}

	}

}
